<h3>Alpha-/Entwicklerversionen</h3>

<b>Helfen Sie beim Testen!</b> Wir entwickeln gerade einige neue Programmteile
und möchten sehen, wie diese bei anderen Anwendern funktionieren,
bevor wir die nächste stabile Version von dvdisaster veröffentlichen.<p>

Insbesondere erhält dvdisaster gerade Unterstützung für BD-Datenträger,
die noch auf mehr Laufwerken getestet werden muß. Die Methode zum 
<a href="howtos90.php">Überprüfen der Kompatibilität von Abbildern</a> ist
sehr hilfreich beim Ausprobieren neuer Laufwerke. Bitte 
<a href="feedback.php">berichten Sie sowohl gute als auch schlechte Ergebnisse</a>
damit wir einen besseren Überblick erhalten, wie die Unterstützung der neuen
Formate gelungen ist.<p>

<b>Ein Wort der Vorsicht:</b> Die Alpha-Versionen können das Laufwerk und/oder
das Betriebssystem abstürzen lassen und einen Neustart erfordern. Sie können
andere Fehler enthalten und sollten nicht zum Bearbeiten von wichtigen Daten
verwendet werden.<p>

Verwenden Sie im Zweifelsfall die <a href="download.php">stabile Version 0.70</a>
und warten Sie auf die Veröffentlichung von Version 0.72.

